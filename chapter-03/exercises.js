var min = (num1, num2) => {return num1 < num2 ? num1 : num2};

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
  if(num % 2 === 0) {
    return true;
  } else {
    return false;
  }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  let count = 0;
  for (let i = 0; i < string.length; i++) {
    if(string[i] === char) {
      count++;
    }
  }
  return count;

}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  let count = 0;
  for (let i = 0; i < string.length; i++) {
    if(string[i] === 'B') {
      count++;
    }
  }
  return count;
}

